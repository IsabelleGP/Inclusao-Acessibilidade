			<p>Em 6 de julho de 2015, foi criada uma lei de inclusão da pessoa com deficiência, ela visa certificar que portadores de necessidades físicas possam participar de atividades, desde as mais complicadas até as mais cotidianas como por exemplo poder atravessar uma rua, entretanto, ainda sim muitos destes não se sentem totalmente inclusos na sociedade de maneira que suas necessidades sejam atendidas devido ao fato de que ainda existem muitos riscos que podem comprometer sua integridade física. Alguns dos paradigmas que devem ser levados em consideração é a falta de piso tátil, piso irregular, obstáculos aéreos e a falta de sinalização visual e auditiva.</p>
			<p>A acessibilidade para pessoas com deficiência tem o intuito de facilitar seu trajeto, sua saúde, utilização de serviços e espaços urbanos, tendo em vista que nem todas as pessoas possuem a mesma necessidade. Apesar da Constituição Federal de 1988 garantir direitos e acessibilidade para essas pessoas, eles não são respeitados, trazendo o desinteresse profissional e o descrédito, por exemplo.</p>
			<p>Mais da metade da população brasileira utiliza o transporte público como principal meio de locomoção, mas a falta de acessibilidade nesses veículos causam diversos transtornos para a população. Existem diversos fatores que atrapalham os PCDs (Pessoas Com Deficiência) a utilizarem os transportes públicos: degraus de altura elevada, falta de mecanismos que ajudem cadeirantes a subirem no veículo, entre outros. Mas o problema não está somente dentro dos ônibus. A falta de sinais sonoros para que o deficiente visual possa saber em qual ponto irá descer, qual onibus irá pegar ou quando poderá atravessar a rua também é uma grande barreira. Mas essas dificuldades podem ser solucionadas com tecnologias alternativas. No caso dos degraus altos e a falta de mecanismos, o piso rebaixado é uma solução fácil e simples.</p>
			<p>Além da acessibilidade nos transportes públicos, é de muitíssima importância o acesso facilitado de pessoas com deficiência em ambientes educacionais, como escolas e faculdades. A partir do momento em que você tira a acessibilidade de uma pessoa em um meio acadêmico acaba-se retirando um direito básico da pessoa, já que sem os determinados equipamentos a pessoa não tem como se locomover nesse espaço, ficando sem condições de estudar. O preconceito, discriminação, incapacidade física e financeira são alguns quesitos que impedem o acesso à educação e saúde. De acordo com a Organização Pan-Americana da Saúde (OPAS), apenas 2% dos 85 milhões de pessoas com deficiência têm toda a assistência adequada que precisam e 10% da população mundial tem algum tipo de deficiência e não possui essa assistência. A Assembléia Mundial da Saúde relatou em maio de 2015 que 80% das PCDs possuem baixa-renda e não tem condições de fazerem um tratamento ou outro serviço de saúde
